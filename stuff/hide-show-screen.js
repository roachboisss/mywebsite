function dim() {
    document.getElementById("dimmer").style.display = "block";
    document.getElementById("showscreen").style.display = "block";
    document.getElementById("hidescreen").style.display = "none";
  }
  function undim() {
    document.getElementById("dimmer").style.display = "none";
    document.getElementById("showscreen").style.display = "none";
    document.getElementById("hidescreen").style.display = "block";
  }